public void cargarDatosProductosFormulario(){
    int fila = tblProducto.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(null, "Se debe seleccionar un registro");
    }else{
        dtmt = (DefaultTableModel) tblProducto.getModel();
        prod.setIdProducto(Integer.parseInt((String)dtmt.getValueAt(fila, 0)));
        prod.setDescripcion((String) dtmt.getValueAt(fila, 1));
        prod.setCodigo((String) dtmt.getValueAt(fila, 2));
        prod.setUnidadMedida((String) dtmt.getValueAt(fila, 3));
        
        
        lblInfoProducto.setText(prod.getIdProducto()+" , "+prod.getDescripcion()+" , "+prod.getCodigo()+" , "+prod.getUnidadMedida());

    }
}

