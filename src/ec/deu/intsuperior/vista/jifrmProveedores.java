public void buscarProveedor() {
        String titulos[] = {"IdProveedor", "Nombres", "Nombre de Contacto", "RUC", "Correo", "Telefono","Direccion", "Pagina Web"};
        dtm.setColumnIdentifiers(titulos);

        Proveedores prov = new Proveedores();
        
        busqueda = txtBuscar.getText();
        if (rbtnIdProveedor.isSelected()) {
            criterio = "idProveedor";
        }else if (rbtnNombre.isSelected()) {
            criterio = "nombres";
        } else if (rbtnNombreContacto.isSelected()) {
            criterio = "nombreContacto";
        } else if (rbtnRuc.isSelected()) {
            criterio = "ruc";
        }
        try {
            rs = prov.listarProveedoresPorParametro(criterio, busqueda);
            boolean encuentra = false;
            
            String datos[] = new String[8];
            //Borrar registros de dtm existes
            int f, i;
            f = dtm.getRowCount();
            if (f > 0) {
                for (i = 0; i < f; i++) {
                    dtm.removeRow(0);
                }
            }
            while (rs.next()) {
                datos[0] = (String) rs.getString(1);
                datos[1] = (String) rs.getString(2);
                datos[2] = (String) rs.getString(3);
                datos[3] = (String) rs.getString(4);
                datos[4] = (String) rs.getString(5);
                datos[5] = (String) rs.getString(6);
                datos[6] = (String) rs.getString(7);
                datos[7] = (String) rs.getString(8);
                dtm.addRow(datos);
                encuentra = true;

            }
            if (encuentra = false) {
                JOptionPane.showMessageDialog(null, "Â¡No se encuentra!");
            }
               f = dtm.getRowCount();
            lblTotalRegistros.setText(String.valueOf(f));

        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
        tblProveedores.setModel(dtm);
    }

